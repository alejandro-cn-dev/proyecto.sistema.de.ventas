    private void cargarCliente() {
        try {
            ResultSet rs2 = crud.selectXtodas();
            while (rs2.next()) {
                Object row2[] = new Object[10];
                for (int i = 0; i < 8; i++) {
                    row2[i] = rs2.getObject(i + 1);
                }
                jComboBox1.addItem(new Cliente(Integer.parseInt(row2[0].toString()), row2[1].toString(), row2[2].toString(), row2[3].toString(), row2[4].toString(), row2[5].toString(), Integer.parseInt(row2[6].toString()), row2[7].toString()));
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar los datos de la Base de Datos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    private void cargarCaja() {
        try {
            ResultSet rs2 = caja.selectXtodas();
            while (rs2.next()) {
                Object row2[] = new Object[10];
                for (int i = 0; i < 3; i++) {
                    row2[i] = rs2.getObject(i + 1);
                }
                jComboBox2.addItem(new Caja(Integer.parseInt(row2[0].toString()), row2[1].toString(), row2[2].toString()));
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar los datos de la Base de Datos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
    private void LimpiarTodo(){
        limpiarTabla();
        FINAL = 0.0;
        jTextField3.setText(String.valueOf(FINAL));
        txtDia.setText("");
        txtMes.setText("");
        txtAnio.setText("");
    }
    private boolean esNumero(String x) {
        try {
            Integer.parseInt(x);
            return true;
        } catch (NumberFormatException edv) {
            return false;
        }
    }

    private void limpiarTabla() {
        for (int i = 0; i < modelo.getRowCount(); i++) {
            modelo.removeRow(i);
            i -= 1;
        }
    }
