    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:  
        int ban=0;
        String NIT = jComboBox1.getSelectedItem().toString();
        String fecha = txtAnio.getText() + "-" + txtMes.getText() + "-" + txtDia.getText();
        String caja = jComboBox2.getSelectedItem().toString();
        if (crud2.insert(Integer.parseInt(NIT), fecha, Integer.parseInt(caja), FINAL)) {
            for (int row = 0; row < modelo.getRowCount(); row++) {
                String cantidad = modelo.getValueAt(row, 0).toString();
                String detalle = modelo.getValueAt(row, 1).toString();
                String total = modelo.getValueAt(row, 3).toString();
                int stockAntes=0,stockDespues=0;
                try {
                    ResultSet rest = crud2.consultaMM(Integer.parseInt(NIT), fecha, Integer.parseInt(caja));
                    ResultSet fel = crud4.ReqID(detalle);
                    ResultSet sk = pro.ObtenerStock(detalle);
                    Object elem[] = new Object[4];
                    while (rest.next()) {
                        elem[0] = rest.getObject(1);
                    }
                    while (fel.next()){
                        elem[1] = fel.getObject(1);
                    }
                    while (sk.next()){
                        elem[2] =sk.getObject(1);
                    }
                    stockAntes=Integer.parseInt(elem[2].toString());
                    stockDespues=stockAntes-Integer.parseInt(cantidad);
                    if (crud3.insert(Integer.parseInt(elem[0].toString()), Integer.parseInt(elem[1].toString()), Integer.parseInt(cantidad), Double.parseDouble(total))) {
                        ban=1;     
                        crud4.ReducirStock(Integer.parseInt(elem[1].toString()), stockDespues);
                    } else {
                        JOptionPane.showMessageDialog(this, "Error al registrar los datos de Detalle.", "Error", JOptionPane.ERROR_MESSAGE);
                    }
                } catch (Exception e) {
                    e.printStackTrace();
                    JOptionPane.showMessageDialog(this, "Error al cargar los datos de la Base de Datos.", "Error", JOptionPane.ERROR_MESSAGE);
                }
            }
            LimpiarTodo();
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar los datos de factura.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        if(ban==1){
            JOptionPane.showMessageDialog(this, "Venta registrada con exito.", "Exito", JOptionPane.INFORMATION_MESSAGE);
        }else{
            JOptionPane.showMessageDialog(this, "Hubo un error al registrar la venta.", "Error", JOptionPane.INFORMATION_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        String CB = jTextField1.getText(), CC = jTextField2.getText();
        if (CB.trim().length() == 0 || CC.trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Falta LLenar Algunos Datos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        if (!esNumero(CB) && !esNumero(CC)) {
            JOptionPane.showMessageDialog(this, "El codigo de barra y la cantidad deben ser numeros", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int cb = Integer.parseInt(CB);
        int cant = Integer.parseInt(CC);
        Double total = 0.0;
        try {
            ResultSet rs = crud4.selectXtodasXID(cb);
            while (rs.next()) {
                Object row[] = new Object[4];
                row[0] = (Object) cant;
                for (int i = 1; i <= 2; i++) {
                    row[i] = rs.getObject(i);
                }
                total = Math.floor(Double.parseDouble(row[2].toString()) * cant);
                row[3] = (Object) total;
                modelo.addRow(row);
                FINAL = FINAL + total;
            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar los datos de la Base de Datos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
        FINAL=Math.floor(FINAL);
        jTextField1.setText("");
        jTextField2.setText("");
        jTextField3.setText(String.valueOf(FINAL));
    }//GEN-LAST:event_jButton1ActionPerformed

