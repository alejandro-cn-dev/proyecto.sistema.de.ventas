    private void jComboBox1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jComboBox1ActionPerformed
        // TODO add your handling code here:
        Cliente est = (Cliente) jComboBox1.getSelectedItem();
        if (est == null) {
            return;
        }
        jTextField2.setText(est.getIdCliente() + "");
        jTextField3.setText(est.getNombre());
        jTextField4.setText(est.getApellido());
        jTextField5.setText(est.getDireccion());
        jTextField6.setText(est.getFechaNac());
        jTextField7.setText(String.valueOf(est.getTelefono()));
        jTextField8.setText(est.getEmail());
        for (int i = 0; i < jComboBox2.getItemCount(); i++) {
            if (est.getTipoId().equals(jComboBox2.getItemAt(i).toString())) {
                jComboBox2.setSelectedIndex(i);
                break;
            }
        }
    }//GEN-LAST:event_jComboBox1ActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        Cliente est = (Cliente) jComboBox1.getSelectedItem();
        if (est == null) {
            return;
        }
        int res = JOptionPane.showConfirmDialog(this, "Desea eliminar los datos del estudiante", "Eliminar", JOptionPane.YES_NO_OPTION, JOptionPane.QUESTION_MESSAGE);
        if (res == JOptionPane.YES_OPTION) {
            String id = jTextField2.getText();
            if (crud.delete(Integer.parseInt(id))) {
                limpiarTodo();
                JOptionPane.showMessageDialog(this, "El registro se elimino con exito", "Exito", JOptionPane.INFORMATION_MESSAGE);
                cargarCliente();
            }else{
                JOptionPane.showMessageDialog(this, "No se pudo eliminar el registro ", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void cargarCliente() {
        try {
            ResultSet rs = crud.selectXtodas();
            while (rs.next()) {
                Object row[] = new Object[8];
                for (int i = 0; i < 8; i++) {
                    row[i] = rs.getObject(i + 1);
                }
                //Agregamos cada registro al modelo de la tabla
                jComboBox1.addItem(new Cliente(Integer.parseInt(row[0].toString()), row[1].toString(), row[2].toString(), row[3].toString(), row[4].toString(), row[5].toString(),
                        Integer.parseInt(row[6].toString()), row[7].toString()));

            }
        } catch (Exception e) {
            e.printStackTrace();
            JOptionPane.showMessageDialog(this, "Error al cargar los datos de la Base de Datos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }

