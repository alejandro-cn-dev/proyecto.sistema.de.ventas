    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        // TODO add your handling code here:
        int id;
        try {
            id = Integer.parseInt(jTextField2.getText());
        } catch (Exception e) {
            JOptionPane.showMessageDialog(this, "Ninguna  Seleccionada.", "Error", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        String ID = jTextField2.getText();
        String nombre = jTextField3.getText();
        String apellido = jTextField4.getText();
        String direccion = jTextField5.getText();
        String fecha = jTextField6.getText();
        String telefono = jTextField7.getText();
        String email = jTextField8.getText();
        if (ID.trim().length() == 0 || nombre.trim().length() == 0 || apellido.trim().length() == 0 || direccion.trim().length() == 0 || fecha.trim().length() == 0) {
            JOptionPane.showMessageDialog(this, "Falta LLenar Algunos Datos", "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        int tel = Integer.parseInt(jTextField7.getText());
        String TipoId = jComboBox2.getSelectedItem().toString();
        if (crud.update(id, TipoId, nombre, apellido, direccion, fecha, tel, email)) {
            JOptionPane.showMessageDialog(this, "Estudiante Actualizado con exito.", "Exito", JOptionPane.INFORMATION_MESSAGE);
            limpiarTodo();
            cargarCliente();
        } else {
            JOptionPane.showMessageDialog(this, "Error al registrar los datos.", "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_jButton4ActionPerformed

